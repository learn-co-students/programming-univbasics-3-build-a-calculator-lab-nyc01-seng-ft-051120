def addition(num1, num2)
  sum = num1 + num2
  return sum
end

def subtraction(num1, num2)
  minus = num1 - num2
  return minus
end

def division(num1, num2)
  div = num1 / num2
  return div
end

def multiplication(num1, num2)
  multi = num1 * num2
  return multi
end

def modulo(num1, num2)
  remainder = num1 % num2
  return remainder
end

def square_root(num)
  squarert = Math.sqrt(num)
  return squarert
end
